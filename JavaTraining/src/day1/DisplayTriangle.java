// Display triangle using single system .out 
public class DisplayTriangle {

    public static void main(String[] args) {
        //square
        System.out.println("----------------");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("|\t\t|");
        System.out.println("----------------");

        //triangle
        System.out.println("\t\t\t*\n\t\t*\t*\t*\n\t*\t*\t*\t*\t*\n*\t*\t*\t*\t*\t*\t*");

//       Escape Sequence 
//
//    \n      new line or carriage return 
//    \t      tab space '     '
//    \\      backward slash 
//    \"      double quotes
        System.out.println(" / ");
        System.out.println(" \\ to display a backward slash / win slash ");
        System.out.println(" \n to display new line ");
        System.out.println(" \t to display tab space");
        System.out.println("  'Some Quote' - Bruce Lee ");
        System.out.println("  \"Some Quote\" - Bruce Lee ");

    }
}
