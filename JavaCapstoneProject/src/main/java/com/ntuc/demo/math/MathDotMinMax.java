
package com.ntuc.demo.math;

public class MathDotMinMax {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;
        int small = Math.min(x, y);
        System.out.println("small-->"+small);
        int big = Math.max(x, y);
        System.out.println("big-->"+big);
    }
}
