
package com.ntuc.demo.operators;

public class ArithmeticOperators {
     public static void main(String args[]) {
        int x = 5;
        int y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(++y);
        System.out.println(x++);
    }
}
