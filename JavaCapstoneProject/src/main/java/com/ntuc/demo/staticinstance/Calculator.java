
package com.ntuc.demo.staticinstance;

public class Calculator {
    public int add(int i, int j){
       return i + j; 
    }
    public int sub(int i, int j){
       return i - j; 
    }
    public int multi(int i, int j){
       return i * j; 
    }
    public int div(int i, int j){
       return i / j; 
    }
    public double add_double(double i, double j){
       return i + j; 
    }
    public double sub_double(double i, double j){
       return i - j; 
    }
    public double multi_double(double i, double j){
       return i * j; 
    }
    public double div_double(double i, double j){
       return i / j; 
    }
}
